#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*p;
	size_t	i;

	i = 0;
	p = (char *) s;
	while (n > i)
	{
		p[i] = (unsigned char) c;
		i++;
	}
	return (s);
}
